#[test]
fn test_full_config_document_parses() {
    let config: BridgeConfig = toml::from_str(FULL_CONFIG_TOML).unwrap();

    assert_eq!(config.bth.confirmations_required, 2);
    assert_eq!(
        config.bth.reserve_address.as_deref(),
        Some("bth_reserve_addr")
    );
    assert_eq!(config.bth.release_threshold, 2);
    assert!(config.bth.spend_key_file.is_none());

    assert_eq!(config.ethereum.chain_id, 11155111);
    assert_eq!(
        config.ethereum.safe_address.as_deref(),
        Some("0x2222222222222222222222222222222222222222")
    );
    assert_eq!(config.ethereum.mint_signers.len(), 2);
    assert_eq!(config.ethereum.mint_threshold, 2);
    assert!(matches!(
        config.ethereum.gas_price_strategy,
        GasPriceStrategy::Fixed(30)
    ));

    assert!(matches!(
        config.solana.commitment,
        SolanaCommitment::Confirmed
    ));
    assert_eq!(config.solana.mint_threshold, 1);

    assert_eq!(config.bridge.fee_bps, 25);
    assert_eq!(config.bridge.min_fee, 500_000_000);
    assert!(config.bridge.testnet);
    // Unspecified knobs fall back to defaults.
    assert_eq!(config.bridge.max_retries, 3);
    assert!(!config.bridge.paused);

    assert_eq!(config.reserve.reconcile_interval_secs, 30);
}

#[test]
fn test_config_from_file_happy_and_error_paths() {
    // Happy path: a real file on disk round-trips through from_file.
    let dir = tempfile::tempdir().unwrap();
    let path = dir.path().join("bridge.toml");
    std::fs::write(&path, FULL_CONFIG_TOML).unwrap();
    let config = BridgeConfig::from_file(path.to_str().unwrap()).unwrap();
    assert_eq!(config.ethereum.chain_id, 11155111);

    // Missing file: readable error, no panic.
    let missing = BridgeConfig::from_file(dir.path().join("nope.toml").to_str().unwrap());
    assert!(missing.unwrap_err().contains("Failed to read config"));

    // Malformed TOML: parse error, no panic.
    let bad_path = dir.path().join("bad.toml");
    std::fs::write(&bad_path, "this is [not toml").unwrap();
    let bad = BridgeConfig::from_file(bad_path.to_str().unwrap());
    assert!(bad.unwrap_err().contains("Failed to parse config"));
}

#[test]
fn test_default_config_round_trips_through_toml() {
    // Every field of the default config serializes and parses back — a
    // config file generated by tooling from the defaults stays loadable.
    let config = BridgeConfig::default();
    let serialized = toml::to_string(&config).unwrap();
    let reparsed: BridgeConfig = toml::from_str(&serialized).unwrap();

    assert_eq!(reparsed.bridge.fee_bps, config.bridge.fee_bps);
    assert_eq!(reparsed.ethereum.chain_id, config.ethereum.chain_id);
    assert_eq!(
        reparsed.reserve.reconcile_interval_secs,
        config.reserve.reconcile_interval_secs
    );
    assert!(matches!(
        reparsed.ethereum.gas_price_strategy,
        GasPriceStrategy::Medium
    ));
    assert!(matches!(
        reparsed.solana.commitment,
        SolanaCommitment::Finalized
    ));
}

#[test]
fn test_gas_price_strategy_variants_parse() {
    #[derive(serde::Deserialize)]
    struct Wrap {
        gps: GasPriceStrategy,
    }

    let low: Wrap = toml::from_str(r#"gps = "low""#).unwrap();
    assert!(matches!(low.gps, GasPriceStrategy::Low));
    let medium: Wrap = toml::from_str(r#"gps = "medium""#).unwrap();
    assert!(matches!(medium.gps, GasPriceStrategy::Medium));
    let high: Wrap = toml::from_str(r#"gps = "high""#).unwrap();
    assert!(matches!(high.gps, GasPriceStrategy::High));
    let fixed: Wrap = toml::from_str("gps = { fixed = 42 }").unwrap();
    assert!(matches!(fixed.gps, GasPriceStrategy::Fixed(42)));

    // Unknown strategy is a config error, not a silent default.
    assert!(toml::from_str::<Wrap>(r#"gps = "warp""#).is_err());
}

#[test]
fn test_solana_commitment_variants_parse() {
    #[derive(serde::Deserialize)]
    struct Wrap {
        c: SolanaCommitment,
    }

    let processed: Wrap = toml::from_str(r#"c = "processed""#).unwrap();
    assert!(matches!(processed.c, SolanaCommitment::Processed));
    let confirmed: Wrap = toml::from_str(r#"c = "confirmed""#).unwrap();
    assert!(matches!(confirmed.c, SolanaCommitment::Confirmed));
    let finalized: Wrap = toml::from_str(r#"c = "finalized""#).unwrap();
    assert!(matches!(finalized.c, SolanaCommitment::Finalized));
    assert!(toml::from_str::<Wrap>(r#"c = "instant""#).is_err());
}

#[test]
fn test_ethereum_federation_knobs_default_and_parse() {
    // A pre-federation config still parses with fail-safe defaults
    // (empty signer set, zero threshold => dev-stub attestation only).
    let legacy: EthereumConfig = toml::from_str(
        r#"
        rpc_url = "http://localhost:8545"
        wbth_contract = "0x0000000000000000000000000000000000000000"
        chain_id = 1
        "#,
    )
    .unwrap();
    assert!(legacy.mint_signers.is_empty());
    assert_eq!(legacy.mint_threshold, 0);
    assert!(legacy.safe_address.is_none());
    assert_eq!(legacy.confirmations_required, 12, "default depth");

    // Solana federation knobs default the same way.
    let sol_legacy: SolanaConfig = toml::from_str(
        r#"
        rpc_url = "http://localhost:8899"
        wbth_program = "11111111111111111111111111111111"
        "#,
    )
    .unwrap();
    assert!(sol_legacy.mint_signers.is_empty());
    assert_eq!(sol_legacy.mint_threshold, 0);
}

#[test]
fn test_bth_config_requires_urls() {
    // rpc_url / ws_url have no defaults: omitting them is a parse error
    // (fail-closed), not a silently-wrong localhost fallback.
    assert!(toml::from_str::<BthConfig>("").is_err());
    assert!(toml::from_str::<BthConfig>(r#"rpc_url = "http://x""#).is_err());
}

#[test]
fn test_fee_calculation_no_overflow_and_extremes() {
    let mut config = BridgeConfig::default();

    // u64::MAX amount must not overflow (fee math widens through u128).
    config.bridge.fee_bps = 10_000; // 100%
    assert_eq!(config.calculate_fee(u64::MAX), u64::MAX);

    // 100% fee of a normal amount is the amount itself.
    assert_eq!(config.calculate_fee(5_000_000_000_000), 5_000_000_000_000);

    // 0 bps floors at the configured minimum fee.
    config.bridge.fee_bps = 0;
    assert_eq!(config.calculate_fee(u64::MAX), config.bridge.min_fee);

    // Fee on a zero amount is still the minimum (orders below the minimum
    // fee are not economically expressible).
    assert_eq!(config.calculate_fee(0), config.bridge.min_fee);
}

