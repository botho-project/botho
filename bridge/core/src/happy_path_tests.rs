#[test]
fn test_chain_from_str_case_insensitive_and_unknown() {
    assert_eq!("BTH".parse::<Chain>().unwrap(), Chain::Bth);
    assert_eq!("Botho".parse::<Chain>().unwrap(), Chain::Bth);
    assert_eq!("ETHEREUM".parse::<Chain>().unwrap(), Chain::Ethereum);
    assert_eq!("Sol".parse::<Chain>().unwrap(), Chain::Solana);

    let err = "dogecoin".parse::<Chain>().unwrap_err();
    assert!(err.contains("Unknown chain"));
    assert!("".parse::<Chain>().is_err());
}

#[test]
fn test_chain_display_round_trips_through_from_str() {
    for chain in [Chain::Bth, Chain::Ethereum, Chain::Solana] {
        let round = chain.to_string().parse::<Chain>().unwrap();
        assert_eq!(round, chain);
    }
}

#[test]
fn test_chain_serde_lowercase() {
    // The wire format is lowercase (DB rows, API payloads, attestation
    // envelopes all rely on it).
    assert_eq!(
        serde_json::to_string(&Chain::Ethereum).unwrap(),
        r#""ethereum""#
    );
    assert_eq!(serde_json::to_string(&Chain::Bth).unwrap(), r#""bth""#);
    assert_eq!(
        serde_json::from_str::<Chain>(r#""solana""#).unwrap(),
        Chain::Solana
    );
    assert!(serde_json::from_str::<Chain>(r#""Ethereum""#).is_err());
}

#[test]
fn test_eth_address_validation_rejects_non_hex() {
    // Correct length and prefix but a non-hex character.
    let bad = ChainAddress::new(
        Chain::Ethereum,
        "0x1234567890abcdef1234567890abcdef1234567g",
    );
    assert!(bad.validate().unwrap_err().contains("valid hex"));

    // Uppercase hex is accepted (checksummed addresses).
    let checksummed = ChainAddress::new(
        Chain::Ethereum,
        "0x52908400098527886E0F7030069857D2E4169EE7",
    );
    assert!(checksummed.validate().is_ok());
}

#[test]
fn test_solana_address_validation_bounds() {
    let valid = ChainAddress::new(
        Chain::Solana,
        "9xQeWvG816bUx9EPjHmaT23yvVM2ZWbrrpZb9PusVFin",
    );
    assert!(valid.validate().is_ok());

    let empty = ChainAddress::new(Chain::Solana, "");
    assert!(empty.validate().is_err());

    let too_short = ChainAddress::new(Chain::Solana, "abc");
    assert!(too_short.validate().unwrap_err().contains("32-44"));

    let too_long = ChainAddress::new(Chain::Solana, "x".repeat(45));
    assert!(too_long.validate().is_err());
}

#[test]
fn test_bth_address_validation() {
    let valid = ChainAddress::new(Chain::Bth, "bth_some_base58_address");
    assert!(valid.validate().is_ok());

    let empty = ChainAddress::new(Chain::Bth, "");
    assert!(empty.validate().unwrap_err().contains("empty"));
}

#[test]
fn test_chain_address_display_and_serde_round_trip() {
    let addr = ChainAddress::new(
        Chain::Ethereum,
        "0x1234567890abcdef1234567890abcdef12345678",
    );
    assert_eq!(
        addr.to_string(),
        "ethereum:0x1234567890abcdef1234567890abcdef12345678"
    );

    let json = serde_json::to_string(&addr).unwrap();
    let back: ChainAddress = serde_json::from_str(&json).unwrap();
    assert_eq!(back, addr);
}
