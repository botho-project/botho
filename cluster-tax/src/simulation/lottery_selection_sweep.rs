//! CT-compatible lottery-selection sweep — the design-research gate for the
//! lottery sub-problem of issue #902 (confidential amounts × value-dependent
//! economics).
//!
//! # The problem this sweep exists to answer
//!
//! The redistribution lottery (`cluster-tax/src/lottery.rs`,
//! `botho/src/consensus/lottery.rs`) draws winners with weight
//!
//! ```text
//! weight = value × (max_factor − factor + 1)      // SelectionMode::ClusterWeighted
//! ```
//!
//! This is the incumbent default and the *only* split-invariant progressive
//! mode: because the weight is **linear in the UTXO value**, splitting a
//! position into many outputs never changes the total weight, while the
//! `(max_factor − factor + 1)` tilt keys off cluster provenance (which inherits
//! through splits). See `docs/design/cluster-tilted-redistribution.md`.
//!
//! Under **confidential amounts** (ADR 0006 decision 1) the UTXO `value` is
//! hidden. Reading it in a consensus weight forces a **ZK weighted-sampling
//! proof over hidden values** (a Merkle-sum tree / proof-of-liabilities sort;
//! see #902 §3). That is the single hardest gadget in the CT epic.
//!
//! # The value-free reframe under test
//!
//! The maintainer's insight: the lottery is only hard under CT because the
//! selection rule reads the hidden value. A **value-free** rule keyed only on
//! signals that stay **public even under CT** needs no proof over amounts:
//!
//! - the transaction **fee** (public by ADR 0006), and under demurrage `fee ≈
//!   base + value × factor × rate × time_held` — a monotone proxy for `value ×
//!   factor`;
//! - coin **circulation** (has it been spent-and-recreated recently — public
//!   from creation heights);
//! - cluster **factor/tag** structure (public chain state).
//!
//! This module scores five selection rules head-to-head on the three axes #902
//! cares about — **redistribution** (Δgini vs a burn baseline), **Sybil
//! resistance** (weight gain when a whale splits a position), and
//! **CT-compatibility / leakage** — to determine whether any value-free rule
//! *recovers* ClusterWeighted's redistribution while staying Sybil-resistant
//! and CT-clean, or whether the ZK-sort is unavoidable.
//!
//! # Method (a focused, deterministic Monte-Carlo)
//!
//! Same shape as the shipped [`super::settlement_horizon_sweep`] and
//! [`super::decoy_quantile_sweep`]: a focused deterministic experiment, not the
//! full agent framework, because the lever here is the *selection rule*, not
//! multi-agent transaction flow. It reuses the shipped consensus kernels — the
//! real [`crate::demurrage_charge`] (to compute the public-fee proxy), the real
//! [`crate::ClusterFactorCurve`] (to derive factor classes from cluster
//! wealth), the real [`crate::LotteryCandidate`]/[`crate::SelectionMode`]
//! weights for the Uniform and ClusterWeighted yardsticks (NO second
//! implementation of the incumbent), and the shared
//! [`super::metrics::calculate_gini`]. All numbers in `docs/research/
//! ct-compatible-lottery-selection.md` are regenerated by this module — nothing
//! is hand-computed. The redistribution track uses expected-value weighting
//! (deterministic, no RNG noise), so the output is byte-for-byte reproducible.

use crate::{
    demurrage_charge, fee_curve::PICO_PER_BTH, ClusterFactorCurve, LotteryCandidate, SelectionMode,
    TagVector,
};

use super::metrics::calculate_gini;

/// Blocks per year at the 5s reference block time (matches the node and the
/// other sweeps).
pub const BLOCKS_PER_YEAR: u64 = 6_307_200;

/// Annual demurrage rate at maximum factor, basis points (2%/yr, #351).
pub const RATE_BPS: u32 = 200;

/// Public per-output base fee floor, in picocredits (0.25 BTH). Every output a
/// transaction creates pays at least this. It is the ONLY real cost a splitter
/// pays per extra ticket, so it is the sole brake on a fee-keyed value-free
/// rule — quantified in the Sybil table.
pub const BASE_FEE_PICO: u128 = PICO_PER_BTH / 4;

/// Circulation weight boost for the value-free `CirculationGated` rule: a coin
/// that has recently been spent-and-recreated (commerce, low idle age) gets
/// this multiplier over an idle hoarded coin.
pub const CIRCULATION_BOOST: u128 = 10;

/// Deterministic seed (kept for structural parity with the other sweeps; the
/// redistribution track is expected-value and needs no draws, but the constant
/// documents the run's identity and lets future stochastic variants hang off
/// it).
pub const SEED: u64 = 0xB07A_0902;

/// A selection rule under test.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum Rule {
    /// Every eligible UTXO has equal weight. Value-free, CT-clean. The "rely on
    /// statistics" null baseline. Sybil resistance is poor (splitting =
    /// tickets).
    Uniform,
    /// Incumbent `value × (max_factor − factor + 1)`. Split-invariant and
    /// progressive, but reads the hidden value → NOT CT-compatible. Included
    /// only as the redistribution yardstick.
    ClusterWeighted,
    /// Value-free: weight ∝ `(max_fee − fee + 1)`. Low public fee (poor /
    /// low-factor / freshly circulated) → more weight. Reads only the public
    /// fee, so CT-clean.
    FeeInverse,
    /// Value-free: a coin that has circulated recently gets
    /// `CIRCULATION_BOOST`× weight, an idle hoarded coin gets 1×. Reads
    /// only public creation age → CT-clean.
    CirculationGated,
    /// Value-free hybrid: `FeeInverse × circulation boost`. CT-clean.
    Hybrid,
}

impl Rule {
    /// All rules in report order.
    pub fn all() -> Vec<Rule> {
        vec![
            Rule::Uniform,
            Rule::ClusterWeighted,
            Rule::FeeInverse,
            Rule::CirculationGated,
            Rule::Hybrid,
        ]
    }

    /// Human label.
    pub fn label(self) -> &'static str {
        match self {
            Rule::Uniform => "Uniform",
            Rule::ClusterWeighted => "ClusterWeighted",
            Rule::FeeInverse => "FeeInverse",
            Rule::CirculationGated => "CirculationGated",
            Rule::Hybrid => "Hybrid(fee×circ)",
        }
    }

    /// Does the rule read the hidden UTXO value? If so it is NOT CT-compatible.
    pub fn reads_hidden_value(self) -> bool {
        matches!(self, Rule::ClusterWeighted)
    }

    /// CT-compatible iff it never reads the hidden value (keys only on the
    /// public fee, public age/circulation, or nothing).
    pub fn is_ct_clean(self) -> bool {
        !self.reads_hidden_value()
    }

    /// One-line justification of the CT-compatibility verdict.
    pub fn ct_reason(self) -> &'static str {
        match self {
            Rule::Uniform => "reads nothing about value (equal weight)",
            Rule::ClusterWeighted => "weight ∝ hidden value → needs a ZK sort over amounts",
            Rule::FeeInverse => "reads only the public fee (already public under ADR 0006)",
            Rule::CirculationGated => "reads only public creation age / circulation",
            Rule::Hybrid => "reads only public fee + public age",
        }
    }

    /// Incremental metadata leakage beyond what ADR 0006 already makes public.
    pub fn leakage(self) -> &'static str {
        match self {
            Rule::Uniform => "none",
            Rule::ClusterWeighted => "N/A (not CT-compatible)",
            // The fee is already public by design, so keying on it adds nothing
            // the §9 metadata table does not already account for.
            Rule::FeeInverse => "none beyond the already-public fee",
            Rule::CirculationGated => "none (creation heights already public)",
            Rule::Hybrid => "none beyond the already-public fee + age",
        }
    }
}

/// A holder / UTXO in the population.
#[derive(Clone, Copy, Debug)]
pub struct Holder {
    /// On-chain wealth in picocredits (the hidden value under CT).
    pub wealth_pico: u128,
    /// Cluster factor in FACTOR_SCALE units (1000..=6000), from the real curve.
    pub factor_scaled: u64,
    /// Has this coin circulated recently (spent-and-recreated / commerce)?
    /// Value-free public signal.
    pub circulated: bool,
}

/// The public fee proxy for a holding, in picocredits:
/// `base_fee + demurrage_owed(value, factor, elapsed)`. This is the CT-public
/// signal a value-free rule keys on. `elapsed_blocks` is the coin's public idle
/// age (a freshly circulated coin has a small elapsed → a fee near the base
/// floor).
pub fn public_fee_pico(wealth_pico: u128, factor_scaled: u64, elapsed_blocks: u64) -> u128 {
    // demurrage_charge takes a u64 value in base units; coin values here are
    // <= tens of millions of BTH (< u64::MAX picocredits), so the cast is safe.
    let value_u64 = u64::try_from(wealth_pico).unwrap_or(u64::MAX);
    let dem = demurrage_charge(
        value_u64,
        factor_scaled,
        elapsed_blocks,
        RATE_BPS,
        BLOCKS_PER_YEAR,
    ) as u128;
    BASE_FEE_PICO + dem
}

/// Compute the selection weight for one holder under a rule.
///
/// - `value_bth` — the holder's value in whole BTH (the ClusterWeighted /
///   Uniform yardsticks are computed via the REAL [`LotteryCandidate::weight`]
///   at BTH scale to avoid any second implementation of the incumbent).
/// - `max_value_bth` — population max value in BTH (Hybrid normalization input
///   to the kernel; unused by the modes we call but kept for fidelity).
/// - `fee_pico`, `max_fee_pico` — the public fee proxy and its population max.
pub fn weight(
    rule: Rule,
    value_bth: u64,
    factor_scaled: u64,
    max_value_bth: u64,
    fee_pico: u128,
    max_fee_pico: u128,
    circulated: bool,
) -> u128 {
    match rule {
        // Yardsticks: use the REAL consensus kernel weights, not a re-derivation.
        Rule::Uniform => real_kernel_weight(
            SelectionMode::Uniform,
            value_bth,
            factor_scaled,
            max_value_bth,
        ),
        Rule::ClusterWeighted => real_kernel_weight(
            SelectionMode::ClusterWeighted,
            value_bth,
            factor_scaled,
            max_value_bth,
        ),
        // Value-free: low public fee → more weight. `+1` avoids a zero weight
        // for the single max-fee coin.
        Rule::FeeInverse => max_fee_pico.saturating_sub(fee_pico) + 1,
        // Value-free: circulated coins get the boost, idle coins get 1.
        Rule::CirculationGated => {
            if circulated {
                CIRCULATION_BOOST
            } else {
                1
            }
        }
        // Value-free compound.
        Rule::Hybrid => {
            let fee_term = max_fee_pico.saturating_sub(fee_pico) + 1;
            let circ = if circulated { CIRCULATION_BOOST } else { 1 };
            fee_term.saturating_mul(circ)
        }
    }
}

/// The incumbent weights via the shipped [`LotteryCandidate::weight`] — this is
/// the reuse contract: the Uniform and ClusterWeighted columns are the exact
/// consensus numbers, not a copy.
fn real_kernel_weight(
    mode: SelectionMode,
    value_bth: u64,
    factor_scaled: u64,
    max_value_bth: u64,
) -> u128 {
    let candidate =
        LotteryCandidate::new([0u8; 36], value_bth, factor_scaled, &TagVector::new(), 0);
    candidate.weight(mode, max_value_bth.max(1))
}

