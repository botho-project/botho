/// One population tier for the redistribution experiment.
#[derive(Clone, Copy, Debug)]
pub struct Tier {
    pub label: &'static str,
    pub count: usize,
    pub wealth_bth: u64,
    pub cluster_wealth_bth: u64,
    pub circulated: bool,
}

/// The redistribution population: a large poor/background tier (factor 1,
/// circulated commerce coins) plus a graded wealthy tier spanning the real
/// factor curve (idle hoarders). Factors are derived from the REAL
/// [`ClusterFactorCurve`] at each tier's cluster wealth.
pub fn population_tiers() -> Vec<Tier> {
    vec![
        Tier {
            label: "background (poor)",
            count: 90,
            wealth_bth: 1_000,
            cluster_wealth_bth: 1_000,
            circulated: true,
        },
        // Graded wealthy tier — a factor gradient so FeeInverse (∝ 1/fee) and
        // ClusterWeighted (∝ value·(maxf−f)) are genuinely different functions.
        Tier {
            label: "50k-cluster wealthy",
            count: 4,
            wealth_bth: 5_000,
            cluster_wealth_bth: 50_000,
            circulated: false,
        },
        Tier {
            label: "500k-cluster wealthy",
            count: 3,
            wealth_bth: 50_000,
            cluster_wealth_bth: 500_000,
            circulated: false,
        },
        Tier {
            label: "10M-cluster whale",
            count: 3,
            wealth_bth: 100_000,
            cluster_wealth_bth: 10_000_000,
            circulated: false,
        },
    ]
}

/// Build the flat holder vector from the tiers.
pub fn build_population(tiers: &[Tier]) -> Vec<Holder> {
    let curve = ClusterFactorCurve::default_params();
    let mut holders = Vec::new();
    for t in tiers {
        let factor = curve.factor(t.cluster_wealth_bth as u128 * PICO_PER_BTH);
        for _ in 0..t.count {
            holders.push(Holder {
                wealth_pico: t.wealth_bth as u128 * PICO_PER_BTH,
                factor_scaled: factor,
                circulated: t.circulated,
            });
        }
    }
    holders
}

/// Result of the redistribution experiment for one rule.
#[derive(Clone, Debug)]
pub struct RedistRow {
    pub rule: Rule,
    /// Final Gini after `years` of collect-and-redistribute under this rule.
    pub final_gini: f64,
    /// Δgini = burn_baseline_gini − final_gini (positive = more equalizing).
    pub delta_gini: f64,
    /// Recovery vs ClusterWeighted: delta_gini / delta_gini(ClusterWeighted).
    pub recovery_pct: f64,
    pub ct_clean: bool,
}

/// Run the collect-and-redistribute loop for one rule and return the final
/// Gini.
///
/// Each round: every wealthy holder pays one year of ordinary demurrage into a
/// pool (factor-1 poor pay zero); the pool is then redistributed by the rule's
/// weights (expected value: holder `i` receives `pool × w_i / Σw`). A `burn`
/// world collects the same demurrage but does not redistribute (the Δgini
/// anchor).
fn run_redistribution(holders: &[Holder], years: u64, rule: Option<Rule>) -> f64 {
    let mut wealth: Vec<u128> = holders.iter().map(|h| h.wealth_pico).collect();
    let factor: Vec<u64> = holders.iter().map(|h| h.factor_scaled).collect();
    let circulated: Vec<bool> = holders.iter().map(|h| h.circulated).collect();
    let mut carry: u128 = 0;

    for _ in 0..years {
        // --- Collect one year of demurrage from every holder.
        let mut pool: u128 = carry;
        for i in 0..wealth.len() {
            let value_u64 = u64::try_from(wealth[i]).unwrap_or(u64::MAX);
            let charge = demurrage_charge(
                value_u64,
                factor[i],
                BLOCKS_PER_YEAR,
                RATE_BPS,
                BLOCKS_PER_YEAR,
            ) as u128;
            let charge = charge.min(wealth[i]);
            wealth[i] -= charge;
            pool += charge;
        }

        // --- Redistribute (or burn if rule is None).
        let Some(rule) = rule else {
            // Burn baseline: pool leaves the system; nothing is paid out.
            carry = 0;
            continue;
        };

        // Current-round public-fee proxy + max value, recomputed from live
        // wealth (a coin's fee tracks its current value·factor).
        let max_value_bth = wealth
            .iter()
            .map(|w| (w / PICO_PER_BTH) as u64)
            .max()
            .unwrap_or(1);
        let fees: Vec<u128> = (0..wealth.len())
            .map(|i| {
                // Idle wealthy coins carry a full year of accrued demurrage in
                // their fee; circulated coins are fresh (elapsed ~ 0), so their
                // fee sits at the base floor.
                let elapsed = if circulated[i] { 0 } else { BLOCKS_PER_YEAR };
                public_fee_pico(wealth[i], factor[i], elapsed)
            })
            .collect();
        let max_fee = fees.iter().copied().max().unwrap_or(1);

        let weights: Vec<u128> = (0..wealth.len())
            .map(|i| {
                let value_bth = (wealth[i] / PICO_PER_BTH) as u64;
                weight(
                    rule,
                    value_bth,
                    factor[i],
                    max_value_bth,
                    fees[i],
                    max_fee,
                    circulated[i],
                )
            })
            .collect();
        let total_weight: u128 = weights.iter().sum();

        if total_weight == 0 {
            carry = pool;
            continue;
        }

        // Expected-value payout: holder i receives pool × w_i / Σw. Integer
        // division; the indivisible remainder carries to the next round.
        let mut distributed: u128 = 0;
        for i in 0..wealth.len() {
            let share = pool.saturating_mul(weights[i]) / total_weight;
            wealth[i] += share;
            distributed += share;
        }
        carry = pool - distributed;
    }

    // Gini over BTH-scaled wealth (ratio-invariant; keeps values in u64).
    let final_wealths: Vec<u64> = wealth
        .iter()
        .map(|&w| u64::try_from(w / PICO_PER_BTH).unwrap_or(u64::MAX))
        .collect();
    calculate_gini(&final_wealths)
}

/// Run the redistribution track for all rules.
pub fn redistribution_sweep(holders: &[Holder], years: u64) -> Vec<RedistRow> {
    let burn_gini = run_redistribution(holders, years, None);
    // ClusterWeighted is the recovery yardstick.
    let cw_gini = run_redistribution(holders, years, Some(Rule::ClusterWeighted));
    let cw_delta = (burn_gini - cw_gini).max(1e-12);

    Rule::all()
        .into_iter()
        .map(|rule| {
            let final_gini = run_redistribution(holders, years, Some(rule));
            let delta_gini = burn_gini - final_gini;
            RedistRow {
                rule,
                final_gini,
                delta_gini,
                recovery_pct: delta_gini / cw_delta * 100.0,
                ct_clean: rule.is_ct_clean(),
            }
        })
        .collect()
}

// ===========================================================================
// Track 2 — Sybil resistance (weight gain under splitting)
// ===========================================================================

/// One row of the splitting-attack table for a rule at a split count.
///
/// The metric is the attacker's **share of total lottery weight** in a fixed
/// reference population (all others' weights held constant), unsplit vs split.
/// This is bounded in `[0, 1]` (a splitter can never exceed 100% of the
/// tickets), so it is both interpretable and captures the natural self-limit —
/// the more the attacker splits, the more the denominator grows. `share_gain`
/// is `share_split / share_unsplit`.
#[derive(Clone, Debug)]
pub struct SybilRow {
    pub rule: Rule,
    pub split_k: u32,
    /// Attacker ticket share when holding one unsplit coin.
    pub share_unsplit: f64,
    /// Attacker ticket share after fragmenting into `split_k` fresh coins.
    pub share_split: f64,
    /// share_split / share_unsplit (1.0 = split-invariant; grows toward the
    /// share cap for per-ticket rules).
    pub share_gain: f64,
    pub ct_clean: bool,
}

/// The attacker: a single saturated-whale coin (10M-BTH cluster, factor
/// ~5.745), value 100k BTH, held idle for one year. It fragments into `k` equal
/// coins.
#[derive(Clone, Copy, Debug)]
pub struct SybilAttacker {
    pub value_bth: u64,
    pub factor_scaled: u64,
}

/// Build the default attacker from the real curve.
pub fn default_attacker() -> SybilAttacker {
    let curve = ClusterFactorCurve::default_params();
    let factor = curve.factor(10_000_000u128 * PICO_PER_BTH);
    SybilAttacker {
        value_bth: 100_000,
        factor_scaled: factor,
    }
}

/// Split counts swept.
pub fn split_counts() -> Vec<u32> {
    vec![10, 100, 1_000]
}

/// The reference population the attacker's ticket share is measured against:
/// the redistribution population (which already contains idle 10M-whales, so
/// the population `max_fee` stays high even after the attacker's own coins
/// fragment to the base floor — the realistic case).
fn reference_population() -> Vec<Holder> {
    build_population(&population_tiers())
}

/// Compute the (unsplit_weights, max_value_bth, max_fee_pico) for the reference
/// population plus the attacker's coins. `attacker_coins` are appended so the
/// population maxima account for the attacker.
fn population_weights(rule: Rule, base: &[Holder], attacker_coins: &[Holder]) -> (u128, u128) {
    // (attacker_weight_total, everyone_weight_total)
    let all: Vec<Holder> = base.iter().chain(attacker_coins.iter()).copied().collect();
    let max_value_bth = all
        .iter()
        .map(|h| (h.wealth_pico / PICO_PER_BTH) as u64)
        .max()
        .unwrap_or(1);
    let fees: Vec<u128> = all
        .iter()
        .map(|h| {
            let elapsed = if h.circulated { 0 } else { BLOCKS_PER_YEAR };
            public_fee_pico(h.wealth_pico, h.factor_scaled, elapsed)
        })
        .collect();
    let max_fee = fees.iter().copied().max().unwrap_or(1);

    let mut attacker_w: u128 = 0;
    let mut total_w: u128 = 0;
    for (i, h) in all.iter().enumerate() {
        let value_bth = (h.wealth_pico / PICO_PER_BTH) as u64;
        let w = weight(
            rule,
            value_bth,
            h.factor_scaled,
            max_value_bth,
            fees[i],
            max_fee,
            h.circulated,
        );
        total_w += w;
        if i >= base.len() {
            attacker_w += w;
        }
    }
    (attacker_w, total_w)
}

/// Attacker ticket share for one rule when the whale is split into `k` coins.
pub fn attacker_share(attacker: &SybilAttacker, rule: Rule, k: u32) -> f64 {
    let base = reference_population();
    let piece_value_bth = attacker.value_bth / k as u64;
    // The k fresh split coins (circulated = true, elapsed ≈ 0 → base-floor fee).
    let coins: Vec<Holder> = (0..k)
        .map(|_| Holder {
            wealth_pico: piece_value_bth as u128 * PICO_PER_BTH,
            factor_scaled: attacker.factor_scaled,
            circulated: true,
        })
        .collect();
    let (attacker_w, total_w) = population_weights(rule, &base, &coins);
    if total_w == 0 {
        return 0.0;
    }
    attacker_w as f64 / total_w as f64
}

/// The attacker's unsplit baseline share: a single aged idle whale coin.
fn attacker_share_unsplit(attacker: &SybilAttacker, rule: Rule) -> f64 {
    let base = reference_population();
    let coin = [Holder {
        wealth_pico: attacker.value_bth as u128 * PICO_PER_BTH,
        factor_scaled: attacker.factor_scaled,
        circulated: false, // aged, idle
    }];
    let (attacker_w, total_w) = population_weights(rule, &base, &coin);
    if total_w == 0 {
        return 0.0;
    }
    attacker_w as f64 / total_w as f64
}

/// Run the Sybil track for all rules × split counts.
pub fn sybil_sweep(attacker: &SybilAttacker) -> Vec<SybilRow> {
    let mut rows = Vec::new();
    for rule in Rule::all() {
        let share_unsplit = attacker_share_unsplit(attacker, rule);
        for k in split_counts() {
            let share_split = attacker_share(attacker, rule, k);
            rows.push(SybilRow {
                rule,
                split_k: k,
                share_unsplit,
                share_split,
                share_gain: if share_unsplit > 0.0 {
                    share_split / share_unsplit
                } else {
                    f64::INFINITY
                },
                ct_clean: rule.is_ct_clean(),
            });
        }
    }
    rows
}

/// The extra public fee (in BTH) a splitter pays to fragment into `k` outputs:
/// `(k − 1) × base_fee`. This is the ONLY brake on a fee-keyed value-free rule
/// — the demurrage portion is churn-invariant (conserved), so splitting adds
/// only the per-output base floor.
pub fn split_fee_cost_bth(k: u32) -> f64 {
    (k.saturating_sub(1)) as f64 * (BASE_FEE_PICO as f64 / PICO_PER_BTH as f64)
}

