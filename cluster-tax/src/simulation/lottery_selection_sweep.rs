/// The full sweep report.
#[derive(Clone, Debug)]
pub struct LotterySelectionReport {
    pub tiers: Vec<Tier>,
    pub years: u64,
    pub burn_gini: f64,
    pub initial_gini: f64,
    pub redist_rows: Vec<RedistRow>,
    pub attacker: SybilAttacker,
    pub sybil_rows: Vec<SybilRow>,
}

/// Run the complete lottery-selection sweep with the default configuration.
pub fn run_lottery_selection_sweep() -> LotterySelectionReport {
    let tiers = population_tiers();
    let holders = build_population(&tiers);
    let years = 10;

    let initial: Vec<u64> = holders
        .iter()
        .map(|h| (h.wealth_pico / PICO_PER_BTH) as u64)
        .collect();
    let initial_gini = calculate_gini(&initial);
    let burn_gini = run_redistribution(&holders, years, None);
    let redist_rows = redistribution_sweep(&holders, years);

    let attacker = default_attacker();
    let sybil_rows = sybil_sweep(&attacker);

    LotterySelectionReport {
        tiers,
        years,
        burn_gini,
        initial_gini,
        redist_rows,
        attacker,
        sybil_rows,
    }
}

/// Render the report as Markdown tables (the doc numbers are generated here,
/// never hand-computed).
pub fn to_markdown(report: &LotterySelectionReport) -> String {
    let mut s = String::new();
    let f_scale = ClusterFactorCurve::FACTOR_SCALE as f64;

    // --- Population.
    s.push_str("### Redistribution population (factors from the real `ClusterFactorCurve`)\n\n");
    s.push_str("| tier | count | wealth (BTH) | cluster wealth (BTH) | factor | circulated |\n");
    s.push_str("|------|------:|-------------:|---------------------:|-------:|:----------:|\n");
    let curve = ClusterFactorCurve::default_params();
    for t in &report.tiers {
        let factor = curve.factor(t.cluster_wealth_bth as u128 * PICO_PER_BTH);
        s.push_str(&format!(
            "| {} | {} | {} | {} | {:.3}x | {} |\n",
            t.label,
            t.count,
            t.wealth_bth,
            t.cluster_wealth_bth,
            factor as f64 / f_scale,
            if t.circulated { "yes" } else { "no" },
        ));
    }
    s.push('\n');

    // --- Redistribution table.
    s.push_str("### Track 1 — redistribution (Δgini vs a burn baseline)\n\n");
    s.push_str(&format!(
        "Collect-and-redistribute over {} years. Initial Gini {:.4}; burn baseline \
         (collect demurrage, redistribute nothing) Gini {:.4}. Δgini = burn − rule \
         (higher = more equalizing). Recovery = Δgini ÷ ClusterWeighted's Δgini.\n\n",
        report.years, report.initial_gini, report.burn_gini,
    ));
    s.push_str("| rule | final Gini | Δgini | recovery vs CW | CT-clean |\n");
    s.push_str("|------|-----------:|------:|---------------:|:--------:|\n");
    for r in &report.redist_rows {
        s.push_str(&format!(
            "| {} | {:.4} | {:+.4} | {:.1}% | {} |\n",
            r.rule.label(),
            r.final_gini,
            r.delta_gini,
            r.recovery_pct,
            if r.ct_clean { "yes" } else { "**no**" },
        ));
    }
    s.push('\n');

    // --- Sybil table.
    s.push_str("### Track 2 — Sybil resistance (ticket share under splitting)\n\n");
    let unsplit_share = |rule: Rule| {
        report
            .sybil_rows
            .iter()
            .find(|r| r.rule == rule)
            .map(|r| r.share_unsplit)
            .unwrap_or(f64::NAN)
    };
    s.push_str(&format!(
        "Attacker: one {}-BTH whale coin at factor {:.3}x, fragmented into K fresh equal \
         coins, measured against a fixed reference population of {} holders (which still \
         contains idle whales, so the population max-fee stays high). The metric is the \
         attacker's **share of total lottery weight** (bounded in [0,1] — a splitter can \
         never exceed 100% of tickets). Split-invariant = the share barely moves under a \
         1000-way split (the ClusterWeighted property). The demurrage the whale sheds is \
         churn-invariant, so the only real cost of the split is (K−1)×base_fee = {:.1} BTH \
         at K=1000.\n\n",
        report.attacker.value_bth,
        report.attacker.factor_scaled as f64 / f_scale,
        reference_population().len(),
        split_fee_cost_bth(1_000),
    ));
    s.push_str("| rule | unsplit share | share @K=1000 | split-invariant | CT-clean |\n");
    s.push_str("|------|--------------:|--------------:|:---------------:|:--------:|\n");
    for rule in Rule::all() {
        let row = report
            .sybil_rows
            .iter()
            .find(|r| r.rule == rule && r.split_k == 1_000)
            .unwrap();
        // Split-invariant iff the attacker's ticket share barely moves under a
        // 1000-way split (the ClusterWeighted property).
        let unsplit = unsplit_share(rule);
        let invariant = (row.share_split - unsplit).abs() <= 0.02_f64.max(unsplit * 0.05);
        s.push_str(&format!(
            "| {} | {:.3}% | {:.3}% | {} | {} |\n",
            rule.label(),
            unsplit * 100.0,
            row.share_split * 100.0,
            if invariant { "yes" } else { "**no**" },
            if rule.is_ct_clean() { "yes" } else { "**no**" },
        ));
    }
    s.push('\n');
    // Per-K share progression for the transparency of the self-limit.
    s.push_str("Ticket share by split count K (shows the [0,1] self-limit saturating):\n\n");
    s.push_str("| rule | K=10 | K=100 | K=1000 |\n");
    s.push_str("|------|-----:|------:|-------:|\n");
    for rule in Rule::all() {
        let share = |k: u32| {
            report
                .sybil_rows
                .iter()
                .find(|r| r.rule == rule && r.split_k == k)
                .map(|r| r.share_split * 100.0)
                .unwrap_or(f64::NAN)
        };
        s.push_str(&format!(
            "| {} | {:.3}% | {:.3}% | {:.3}% |\n",
            rule.label(),
            share(10),
            share(100),
            share(1_000),
        ));
    }
    s.push('\n');

    // --- CT-compatibility / leakage summary.
    s.push_str("### CT-compatibility & incremental leakage\n\n");
    s.push_str("| rule | CT-clean | reason | leakage beyond ADR-0006-public |\n");
    s.push_str("|------|:--------:|--------|--------------------------------|\n");
    for rule in Rule::all() {
        s.push_str(&format!(
            "| {} | {} | {} | {} |\n",
            rule.label(),
            if rule.is_ct_clean() { "yes" } else { "**no**" },
            rule.ct_reason(),
            rule.leakage(),
        ));
    }
    s.push('\n');

    s
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn cluster_weighted_is_split_invariant() {
        // The defining property: value-linear weight ⇒ splitting never changes
        // the attacker's ticket share. This is why ClusterWeighted is the
        // incumbent — and why it must read the value.
        let attacker = default_attacker();
        let unsplit = attacker_share_unsplit(&attacker, Rule::ClusterWeighted);
        for k in split_counts() {
            let split = attacker_share(&attacker, Rule::ClusterWeighted, k);
            assert!(
                (split - unsplit).abs() / unsplit < 0.02,
                "ClusterWeighted must be ~split-invariant: unsplit {unsplit}, split {split} at K={k}"
            );
        }
    }

    #[test]
    fn value_free_rules_are_sybil_vulnerable() {
        // Every CT-clean value-free rule reintroduces the per-ticket subsidy:
        // splitting materially increases the attacker's ticket share (the
        // property ClusterWeighted was chosen to avoid). Core negative result.
        let attacker = default_attacker();
        for rule in [
            Rule::Uniform,
            Rule::FeeInverse,
            Rule::CirculationGated,
            Rule::Hybrid,
        ] {
            let unsplit = attacker_share_unsplit(&attacker, rule);
            let split = attacker_share(&attacker, rule, 1_000);
            assert!(
                split > unsplit * 2.0,
                "{} should be Sybil-vulnerable (share grows under splitting): unsplit {unsplit}, split {split}",
                rule.label()
            );
        }
    }

    #[test]
    fn yardstick_weights_match_the_real_kernel() {
        // Reuse contract: the Uniform and ClusterWeighted columns are the exact
        // LotteryCandidate::weight numbers, not a re-derivation.
        let value_bth = 50_000u64;
        let factor = 4_000u64;
        let max_value = 100_000u64;
        let cand = LotteryCandidate::new([0u8; 36], value_bth, factor, &TagVector::new(), 0);
        assert_eq!(
            weight(Rule::Uniform, value_bth, factor, max_value, 0, 0, false),
            cand.weight(SelectionMode::Uniform, max_value)
        );
        assert_eq!(
            weight(
                Rule::ClusterWeighted,
                value_bth,
                factor,
                max_value,
                0,
                0,
                false
            ),
            cand.weight(SelectionMode::ClusterWeighted, max_value)
        );
    }

    #[test]
    fn feeinverse_recovers_most_redistribution() {
        // The positive half of the result: the best value-free rule recovers a
        // large fraction of ClusterWeighted's Δgini.
        let report = run_lottery_selection_sweep();
        let cw = report
            .redist_rows
            .iter()
            .find(|r| r.rule == Rule::ClusterWeighted)
            .unwrap();
        let fi = report
            .redist_rows
            .iter()
            .find(|r| r.rule == Rule::FeeInverse)
            .unwrap();
        assert!(
            cw.delta_gini > 0.0,
            "CW must reduce Gini: {}",
            cw.delta_gini
        );
        assert!(
            fi.delta_gini > 0.0,
            "FeeInverse must reduce Gini: {}",
            fi.delta_gini
        );
        // FeeInverse should recover a substantial share (not a full match).
        assert!(
            fi.recovery_pct > 40.0,
            "FeeInverse should recover a meaningful share of CW, got {:.1}%",
            fi.recovery_pct
        );
    }

    #[test]
    fn ct_flags_are_correct() {
        assert!(!Rule::ClusterWeighted.is_ct_clean());
        for rule in [
            Rule::Uniform,
            Rule::FeeInverse,
            Rule::CirculationGated,
            Rule::Hybrid,
        ] {
            assert!(rule.is_ct_clean(), "{} should be CT-clean", rule.label());
        }
    }

    #[test]
    fn report_is_reproducible() {
        let a = to_markdown(&run_lottery_selection_sweep());
        let b = to_markdown(&run_lottery_selection_sweep());
        assert_eq!(a, b, "sweep output must be byte-for-byte deterministic");
    }

    #[test]
    fn markdown_has_all_sections() {
        let md = to_markdown(&run_lottery_selection_sweep());
        assert!(md.contains("Track 1 — redistribution"));
        assert!(md.contains("Track 2 — Sybil resistance"));
        assert!(md.contains("CT-compatibility"));
        for rule in Rule::all() {
            assert!(md.contains(rule.label()), "missing rule {}", rule.label());
        }
    }
}
